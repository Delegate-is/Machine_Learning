# Getting user input
celsius = float(input("Enter the temperature in Celsius: "))
 
# Converting temperature to Fahrenheit
fahrenheit = (celsius * 9/5) + 32
 
# Displaying output
print("The temperature in Fahrenheit is:", fahrenheit)